package com.syntax.class12;

public class ReplCheckLetterAtEnd {
	
	/*
	  Validate if the string ends with "u" prints 
	  the boolean value accordingly.
	Validate if the string ends with "world" prints 
	the boolean value accordingly.
	Validate if the string ends with "are" prints
	the boolean value accordingly.
	Validate if the string ends with "you" prints
	the boolean value accordingly.
	 */
	public static void main(String[] args) {
		   //starts the code from here
		  String s1="hello how are you"; 
		  System.out.println(s1.endsWith("u"));       
		  System.out.println(s1.endsWith("world"));     
		  System.out.println(s1.endsWith("are")); 
		  System.out.println(s1.endsWith("you"));  
		  }
		
}
